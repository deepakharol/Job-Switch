public class Main {
    public static void main(String[] args) {
        System.out.println("first line of code");
    }

    public static void printMatrix(int[][] matrix) {
        int n = matrix.length;

        // Print the matrix
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                System.out.print(matrix[i][j] + "\t");
            }
            System.out.println();
        }
    }
}